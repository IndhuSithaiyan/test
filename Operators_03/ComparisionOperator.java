package Operators_03;

public class ComparisionOperator {
    public static void main(String[] args) {
        
        int a, b;
        a = 100;
        b = 90;
        System.out.println("a==b : "+ (a==b));
        System.out.println("a!=b : "+ (a!=b));
        System.out.println("a>b : "+ (a>b));
        System.out.println("a<b : "+ (a<b));
        System.out.println("a>=b : "+ (a>=b));
        System.out.println("a<=b : "+ (a<=b));

    }
}

/*
Relation operator or comparision operator
==	Equal to--	x == y	
!=	Not equal--   x != y	
>	Greater than--  x > y	
<	Less than--    x < y	
>=	Greater than or equal to--	x >= y	
<=	Less than or equal to--	x <= y

NOTE: 
-> Relational operators return a boolean result after the comparison.
-> They are mostly used in looping and conditional statements.

*/